															AND L1.GRP_ID = UTGU.GRP_ID
														LEFT OUTER JOIN Vehicle_System.dbo.OFFICETBL OT WITH(NOLOCK)
															ON CT.OFF_KEY = OT.OFF_KEY
														LEFT OUTER JOIN Vehicle_System.dbo.DRIVERTBL DT WITH(NOLOCK)
															ON CT.OFF_KEY = DT.OFF_KEY
															AND CT.DRI_IDX = DT.DRI_IDX
														LEFT OUTER JOIN Vehicle_System.dbo.COMMONCODETBL_KR CODE WITH(NOLOCK)
															ON I_ROOT = ''506''
															AND I_ORDER > 0
															AND CT.WORK_GUBUN = CODE.I_CODE
															LEFT OUTER JOIN  VEHICLE_SYSTEM.dbo.TemperatureConfigTBL AS TEMP_A WITH(NOLOCK) 
																ON CT.OFF_KEY = TEMP_A.OFF_KEY
																AND CT.TID_ID = TEMP_A.TID_ID
															AND TEMP_A.DEVICE_NUM = ''A''
														LEFT OUTER JOIN VEHICLE_SYSTEM.dbo.TemperatureConfigTBL AS TEMP_B WITH(NOLOCK) 
															ON CT.OFF_KEY = TEMP_B.OFF_KEY
															AND CT.TID_ID = TEMP_B.TID_ID
															AND TEMP_B.DEVICE_NUM = ''B''
														LEFT OUTER JOIN  VEHICLE_SYSTEM.dbo.TemperatureConfigTBL AS TEMP_C WITH(NOLOCK) 
															ON CT.OFF_KEY = TEMP_C.OFF_KEY
															AND CT.TID_ID = TEMP_C.TID_ID
															AND TEMP_C.DEVICE_NUM = ''C''
															LEFT OUTER JOIN  VEHICLE_SYSTEM.dbo.TemperatureConfigTBL AS TEMP_D WITH(NOLOCK) 
																ON CT.OFF_KEY = TEMP_D.OFF_KEY
																AND CT.TID_ID = TEMP_D.TID_ID
																AND TEMP_D.DEVICE_NUM = ''D''
															OUTER APPLY (
																SELECT
																	COUNT_BIG(1) AS TOT_CNT,
																	SUM(TEM_CALC.ISSUE_FLAG) AS ISSUE_CNT
																FROM (
																	SELECT
																		CASE
																			WHEN
																				(CALC.TEMPER_A IS NOT NULL AND (CALC.TEMPER_A > ISNULL(TEMP_A.TEMP_MAX, 0) OR CALC.TEMPER_A < ISNULL(TEMP_A.TEMP_MIN, 0)))
																				OR (CALC.TEMPER_B IS NOT NULL AND (CALC.TEMPER_B > ISNULL(TEMP_B.TEMP_MAX, 0) OR CALC.TEMPER_B < ISNULL(TEMP_B.TEMP_MIN, 0)))
																				OR (CALC.TEMPER_C IS NOT NULL AND (CALC.TEMPER_C > ISNULL(TEMP_C.TEMP_MAX, 0) OR CALC.TEMPER_C < ISNULL(TEMP_C.TEMP_MIN, 0)))
																				OR (CALC.TEMPER_D IS NOT NULL AND (CALC.TEMPER_D > ISNULL(TEMP_D.TEMP_MAX, 0) OR CALC.TEMPER_D < ISNULL(TEMP_D.TEMP_MIN, 0)))
																			THEN 1
																			ELSE 0
																		END AS ISSUE_FLAG
																	FROM DBO.TEMPERATURETBL TEM WITH(NOLOCK) --2022.08 BK09로 백업정책 전환
																	CROSS APPLY (
																		SELECT
																			CASE
																				WHEN SUBSTRING(TEM.TPL_DEGREE_A, 1, 1) IN (''N'', ''S'', ''O'') THEN NULL
																				WHEN TEM.TPL_SIGNAL_A = 1 THEN -ABS(TRY_CONVERT(FLOAT, TEM.TPL_DEGREE_A))
																				ELSE TRY_CONVERT(FLOAT, TEM.TPL_DEGREE_A)
																			END AS TEMPER_A,
																			CASE
																				WHEN SUBSTRING(TEM.TPL_DEGREE_B, 1, 1) IN (''N'', ''S'', ''O'') THEN NULL
																				WHEN TEM.TPL_SIGNAL_B = 1 THEN -ABS(TRY_CONVERT(FLOAT, TEM.TPL_DEGREE_B))
																				ELSE TRY_CONVERT(FLOAT, TEM.TPL_DEGREE_B)
																			END AS TEMPER_B,
																			CASE
																				WHEN SUBSTRING(TEM.TPL_DEGREE_C, 1, 1) IN (''N'', ''S'', ''O'') THEN NULL
																				WHEN TEM.TPL_SIGNAL_C = 1 THEN -ABS(TRY_CONVERT(FLOAT, TEM.TPL_DEGREE_C))
																				ELSE TRY_CONVERT(FLOAT, TEM.TPL_DEGREE_C)
																			END AS TEMPER_C,
																			CASE
																				WHEN SUBSTRING(TEM.TPL_DEGREE_D, 1, 1) IN (''N'', ''S'', ''O'') THEN NULL
																				WHEN TEM.TPL_SIGNAL_D = 1 THEN -ABS(TRY_CONVERT(FLOAT, TEM.TPL_DEGREE_D))
																				ELSE TRY_CONVERT(FLOAT, TEM.TPL_DEGREE_D)
																			END AS TEMPER_D
																	) CALC
																	WHERE TID.OFF_KEY = TEM.OFF_KEY
																	AND TID.TID_ID = TEM.TID_ID
																	AND TEM.TPL_DATE BETWEEN ''' + @START_DATE + ''' AND ''' + @END_DATE + '''
																	AND TEM.TPL_DATE + TEM.TPL_TIME BETWEEN ''' + @START_DATE + @START_TIME + '0000'' AND ''' + @END_DATE + @END_TIME + '0001''
																) TEM_CALC
															) TEM_AGG
															WHERE (CT.CM_KEY IS NOT NULL OR CT.CM_KEY <> '''')
														AND TID.TID_USE_FLAG = ''1''
														--AND TEM.TPL_DATE BETWEEN '''+@START_DATE+'''  AND '''+@END_DATE+'''
														--AND TEM.TPL_DATE + TEM.TPL_TIME BETWEEN '''+@START_DATE+@START_TIME+'0000''  AND '''+@END_DATE+@END_TIME+'0001''
														--AND TEM.TPL_DATE BETWEEN '''+@START_DATE+'''  AND '''+@END_DATE+'''
														--AND TEM.TPL_TIME BETWEEN ''' + @START_TIME + ''' + ''0000'' AND ''' + @END_TIME + ''' + ''0001''
														-- 사용자그룹의 차량만 표시 (관리자는 그룹미등록 차량까지 표시)
														AND (
															UTGU.I_USERID = ''' + @SS_I_USERID + '''
															OR (
																''' + @SS_I_CODE + ''' = ''999022''
																AND UTGU.I_USERID IS NULL
															)
														)
														AND CT.OFF_KEY = '''+@SS_OFF_KEY+''' '
		
		-- 창고
		IF @CARGO_YN = 'Y'
			BEGIN
				SET @SQL = @SQL + '						AND CT.CM_CLASS = ''128012'' '
			END
		-- 차량
		ELSE
			BEGIN
				SET @SQL = @SQL + '						AND ISNULL(CT.CM_CLASS,'''') != ''128012'' '
			END

		-- 그룹1 검색
		IF @P_GRP_ID IS NOT NULL AND @P_GRP_ID <> ''
			BEGIN
				IF @P_GRP_ID = '00000000000000'
					BEGIN
						SET @SQL = @SQL + '				AND (L1.P_GRP_ID IS NULL OR L1.P_GRP_ID = '''') '
					END
				ELSE
					BEGIN
						SET @SQL = @SQL + '				AND L1.P_GRP_ID = ''' + @P_GRP_ID + ''' '
					END
			END
		
		-- 그룹2 검색
		IF @GRP_ID IS NOT NULL AND @GRP_ID <> ''
			BEGIN
				SET @SQL = @SQL + '						AND UTGU.GRP_ID = ''' + @GRP_ID + ''' '
			END
		
		-- 차량번호 검색
		IF @CM_NUMBER IS NOT NULL AND @CM_NUMBER <> ''
			BEGIN
				SET @SQL = @SQL + '						AND CT.CM_NUMBER LIKE ''%' + @CM_NUMBER + '%'' '
			END

		-- 단말기번호 검색
		IF @TID_ID IS NOT NULL AND @TID_ID <> ''
			BEGIN
				SET @SQL = @SQL + '						AND CT.TID_ID LIKE ''%' + @TID_ID + '%'' '
			END
		
		-- 업무구분 검색
		IF @WORK_GUBUN IS NOT NULL AND @WORK_GUBUN <> ''
			BEGIN
				SET @SQL = @SQL + '						AND CT.WORK_GUBUN = ''' + @WORK_GUBUN + ''' '
			END

		SET @SQL = @SQL + '							) BA
												WHERE 1=1 
												GROUP BY P_GRP_ID, P_GRP_NAME, GRP_ID, GRP_NAME, OFF_NAME, OFF_KEY, WORK_GUBUN, N_CODENAME, CM_KEY, CM_NUMBER, TID_ID, TEMP_MIN_A, TEMP_MAX_A, TEMP_MIN_B, TEMP_MAX_B, TEMP_MIN_C, TEMP_MAX_C, TEMP_MIN_D, TEMP_MAX_D
											) BA
											LEFT OUTER JOIN Vehicle_System.dbo.TemperatureAlarmConfigTBL AS ALARM_A WITH(NOLOCK) 
												ON BA.OFF_KEY = ALARM_A.OFF_KEY
												AND BA.TID_ID = ALARM_A.TID_ID
												AND ALARM_A.DEVICE_NUM = ''A'' '
		SET @SQL = @SQL + '					OUTER APPLY (
												SELECT
													MAX(CASE WHEN SORT_NUMBER = 1 THEN (CASE WHEN ALARM_SUNDAY = ''Y'' THEN ''1'' ELSE '''' END + CASE WHEN ALARM_MONDAY = ''Y'' THEN ''2'' ELSE '''' END + CASE WHEN ALARM_TUESDAY = ''Y'' THEN ''3'' ELSE '''' END + CASE WHEN ALARM_WEDNESDAY = ''Y'' THEN ''4'' ELSE '''' END + CASE WHEN ALARM_THURSDAY = ''Y'' THEN ''5'' ELSE '''' END + CASE WHEN ALARM_FRIDAY = ''Y'' THEN ''6'' ELSE '''' END + CASE WHEN ALARM_SATURDAY = ''Y'' THEN ''7'' ELSE '''' END + '':'' + ALARM_START + ''~'' + ALARM_END) ELSE '''' END) '
		SET @SQL = @SQL + '							+ MAX(CASE WHEN SORT_NUMBER = 2 THEN ('';'' + CASE WHEN ALARM_SUNDAY = ''Y'' THEN ''1'' ELSE '''' END + CASE WHEN ALARM_MONDAY = ''Y'' THEN ''2'' ELSE '''' END + CASE WHEN ALARM_TUESDAY = ''Y'' THEN ''3'' ELSE '''' END + CASE WHEN ALARM_WEDNESDAY = ''Y'' THEN ''4'' ELSE '''' END + CASE WHEN ALARM_THURSDAY = ''Y'' THEN ''5'' ELSE '''' END + CASE WHEN ALARM_FRIDAY = ''Y'' THEN ''6'' ELSE '''' END + CASE WHEN ALARM_SATURDAY = ''Y'' THEN ''7'' ELSE '''' END + '':'' + ALARM_START + ''~'' + ALARM_END) ELSE '''' END) '
		SET @SQL = @SQL + '							+ MAX(CASE WHEN SORT_NUMBER = 3 THEN ('';'' + CASE WHEN ALARM_SUNDAY = ''Y'' THEN ''1'' ELSE '''' END + CASE WHEN ALARM_MONDAY = ''Y'' THEN ''2'' ELSE '''' END + CASE WHEN ALARM_TUESDAY = ''Y'' THEN ''3'' ELSE '''' END + CASE WHEN ALARM_WEDNESDAY = ''Y'' THEN ''4'' ELSE '''' END + CASE WHEN ALARM_THURSDAY = ''Y'' THEN ''5'' ELSE '''' END + CASE WHEN ALARM_FRIDAY = ''Y'' THEN ''6'' ELSE '''' END + CASE WHEN ALARM_SATURDAY = ''Y'' THEN ''7'' ELSE '''' END + '':'' + ALARM_START + ''~'' + ALARM_END) ELSE '''' END) '
		SET @SQL = @SQL + '							+ MAX(CASE WHEN SORT_NUMBER = 4 THEN ('';'' + CASE WHEN ALARM_SUNDAY = ''Y'' THEN ''1'' ELSE '''' END + CASE WHEN ALARM_MONDAY = ''Y'' THEN ''2'' ELSE '''' END + CASE WHEN ALARM_TUESDAY = ''Y'' THEN ''3'' ELSE '''' END + CASE WHEN ALARM_WEDNESDAY = ''Y'' THEN ''4'' ELSE '''' END + CASE WHEN ALARM_THURSDAY = ''Y'' THEN ''5'' ELSE '''' END + CASE WHEN ALARM_FRIDAY = ''Y'' THEN ''6'' ELSE '''' END + CASE WHEN ALARM_SATURDAY = ''Y'' THEN ''7'' ELSE '''' END + '':'' + ALARM_START + ''~'' + ALARM_END) ELSE '''' END) '
		SET @SQL = @SQL + '							+ MAX(CASE WHEN SORT_NUMBER = 5 THEN ('';'' + CASE WHEN ALARM_SUNDAY = ''Y'' THEN ''1'' ELSE '''' END + CASE WHEN ALARM_MONDAY = ''Y'' THEN ''2'' ELSE '''' END + CASE WHEN ALARM_TUESDAY = ''Y'' THEN ''3'' ELSE '''' END + CASE WHEN ALARM_WEDNESDAY = ''Y'' THEN ''4'' ELSE '''' END + CASE WHEN ALARM_THURSDAY = ''Y'' THEN ''5'' ELSE '''' END + CASE WHEN ALARM_FRIDAY = ''Y'' THEN ''6'' ELSE '''' END + CASE WHEN ALARM_SATURDAY = ''Y'' THEN ''7'' ELSE '''' END + '':'' + ALARM_START + ''~'' + ALARM_END) ELSE '''' END) '
		SET @SQL = @SQL + '							+ MAX(CASE WHEN SORT_NUMBER = 6 THEN ('';'' + CASE WHEN ALARM_SUNDAY = ''Y'' THEN ''1'' ELSE '''' END + CASE WHEN ALARM_MONDAY = ''Y'' THEN ''2'' ELSE '''' END + CASE WHEN ALARM_TUESDAY = ''Y'' THEN ''3'' ELSE '''' END + CASE WHEN ALARM_WEDNESDAY = ''Y'' THEN ''4'' ELSE '''' END + CASE WHEN ALARM_THURSDAY = ''Y'' THEN ''5'' ELSE '''' END + CASE WHEN ALARM_FRIDAY = ''Y'' THEN ''6'' ELSE '''' END + CASE WHEN ALARM_SATURDAY = ''Y'' THEN ''7'' ELSE '''' END + '':'' + ALARM_START + ''~'' + ALARM_END) ELSE '''' END) '
		SET @SQL = @SQL + '							+ MAX(CASE WHEN SORT_NUMBER = 7 THEN ('';'' + CASE WHEN ALARM_SUNDAY = ''Y'' THEN ''1'' ELSE '''' END + CASE WHEN ALARM_MONDAY = ''Y'' THEN ''2'' ELSE '''' END + CASE WHEN ALARM_TUESDAY = ''Y'' THEN ''3'' ELSE '''' END + CASE WHEN ALARM_WEDNESDAY = ''Y'' THEN ''4'' ELSE '''' END + CASE WHEN ALARM_THURSDAY = ''Y'' THEN ''5'' ELSE '''' END + CASE WHEN ALARM_FRIDAY = ''Y'' THEN ''6'' ELSE '''' END + CASE WHEN ALARM_SATURDAY = ''Y'' THEN ''7'' ELSE '''' END + '':'' + ALARM_START + ''~'' + ALARM_END) ELSE '''' END) '
		SET @SQL = @SQL + '							+ MAX(CASE WHEN SORT_NUMBER = 8 THEN ('';'' + CASE WHEN ALARM_SUNDAY = ''Y'' THEN ''1'' ELSE '''' END + CASE WHEN ALARM_MONDAY = ''Y'' THEN ''2'' ELSE '''' END + CASE WHEN ALARM_TUESDAY = ''Y'' THEN ''3'' ELSE '''' END + CASE WHEN ALARM_WEDNESDAY = ''Y'' THEN ''4'' ELSE '''' END + CASE WHEN ALARM_THURSDAY = ''Y'' THEN ''5'' ELSE '''' END + CASE WHEN ALARM_FRIDAY = ''Y'' THEN ''6'' ELSE '''' END + CASE WHEN ALARM_SATURDAY = ''Y'' THEN ''7'' ELSE '''' END + '':'' + ALARM_START + ''~'' + ALARM_END) ELSE '''' END) '
		SET @SQL = @SQL + '							+ MAX(CASE WHEN SORT_NUMBER = 9 THEN ('';'' + CASE WHEN ALARM_SUNDAY = ''Y'' THEN ''1'' ELSE '''' END + CASE WHEN ALARM_MONDAY = ''Y'' THEN ''2'' ELSE '''' END + CASE WHEN ALARM_TUESDAY = ''Y'' THEN ''3'' ELSE '''' END + CASE WHEN ALARM_WEDNESDAY = ''Y'' THEN ''4'' ELSE '''' END + CASE WHEN ALARM_THURSDAY = ''Y'' THEN ''5'' ELSE '''' END + CASE WHEN ALARM_FRIDAY = ''Y'' THEN ''6'' ELSE '''' END + CASE WHEN ALARM_SATURDAY = ''Y'' THEN ''7'' ELSE '''' END + '':'' + ALARM_START + ''~'' + ALARM_END) ELSE '''' END) '
		SET @SQL = @SQL + '							+ MAX(CASE WHEN SORT_NUMBER = 10 THEN ('';'' + CASE WHEN ALARM_SUNDAY = ''Y'' THEN ''1'' ELSE '''' END + CASE WHEN ALARM_MONDAY = ''Y'' THEN ''2'' ELSE '''' END + CASE WHEN ALARM_TUESDAY = ''Y'' THEN ''3'' ELSE '''' END + CASE WHEN ALARM_WEDNESDAY = ''Y'' THEN ''4'' ELSE '''' END + CASE WHEN ALARM_THURSDAY = ''Y'' THEN ''5'' ELSE '''' END + CASE WHEN ALARM_FRIDAY = ''Y'' THEN ''6'' ELSE '''' END + CASE WHEN ALARM_SATURDAY = ''Y'' THEN ''7'' ELSE '''' END + '':'' + ALARM_START + ''~'' + ALARM_END) ELSE '''' END)
													AS ALARM_TIME_A
