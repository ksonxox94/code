									  FROM DBO.TEMPERATURETBL TEM WITH(NOLOCK)		--2022.08 BK09로 백업정책 전환												
									WHERE OFF_KEY = A.OFF_KEY
									AND TID_ID = A.TID_ID
									ORDER BY TPL_DATE DESC, TPL_TIME DESC
								) CUR
								ORDER BY ISNULL(P_GRP_ID, ''Z0000000000000'') DESC, P_GRP_NAME, GRP_NAME,OFF_NAME, CM_NUMBER
								--, ISSUE_CNT
								'
		END
	
	-- 카운트
	ELSE IF @GUBUN = 2
		BEGIN
			SET @SQL = @SQL + '
								SELECT
									COUNT(*) AS TOT_COUNT, 
									SUM(CASE WHEN ISSUE_CNT > 0 THEN 1 ELSE 0 END) AS ERR_COUNT 
								FROM BASEDATA A WITH(NOLOCK) '
		END
	
	-- 페이징
	ELSE IF @GUBUN = 1
		BEGIN
			SET @SQL = @SQL + '
								SELECT
									A.*
									, CASE WHEN ISSUE_CNT > 0 THEN ''Y'' ELSE ''N'' END AS DTG_SENSING 
									, CASE WHEN LEN(A.TID_ID) > 4 THEN ''Y'' ELSE ''N'' END AS DTG_DEVICE
									-- TEMP
									, CUR.TPL_SIGNAL_A, CUR.TPL_DEGREE_A
									, CUR.TPL_SIGNAL_B, CUR.TPL_DEGREE_B
									, CUR.TPL_SIGNAL_C, CUR.TPL_DEGREE_C
									, CUR.TPL_SIGNAL_D, CUR.TPL_DEGREE_D
									, CASE
										WHEN SUBSTRING(CUR.TPL_DEGREE_A,1,1) IN (''N'', ''S'', ''O'') OR CUR.TPL_DEGREE_A IS NULL THEN NULL
										ELSE 
											CASE WHEN CUR.TPL_SIGNAL_A=1 THEN -ABS(CUR.TPL_DEGREE_A)
											ELSE CUR.TPL_DEGREE_A END 
										END AS TEMPER_A
									, CASE WHEN SUBSTRING(CUR.TPL_DEGREE_B,1,1) IN (''N'', ''S'', ''O'') OR CUR.TPL_DEGREE_B IS NULL THEN NULL
										ELSE
											CASE WHEN CUR.TPL_SIGNAL_B=1 THEN -ABS(CUR.TPL_DEGREE_B)
											ELSE CUR.TPL_DEGREE_B END 
										END AS TEMPER_B
									, CASE WHEN SUBSTRING(CUR.TPL_DEGREE_C,1,1) IN (''N'', ''S'', ''O'') OR CUR.TPL_DEGREE_C IS NULL THEN NULL
										ELSE
											CASE WHEN CUR.TPL_SIGNAL_C=1 THEN -ABS(CUR.TPL_DEGREE_C)
											ELSE CUR.TPL_DEGREE_C END 
										END AS TEMPER_C
									, CASE WHEN SUBSTRING(CUR.TPL_DEGREE_D,1,1) IN (''N'', ''S'', ''O'') OR CUR.TPL_DEGREE_D IS NULL THEN NULL
										ELSE
											CASE WHEN CUR.TPL_SIGNAL_D=1 THEN -ABS(CUR.TPL_DEGREE_D)
											ELSE CUR.TPL_DEGREE_D END 
										END AS TEMPER_D

								FROM BASEDATA A WITH(NOLOCK)
								outer APPLY (
									SELECT TOP 1 *
									--FROM DBO.TEMPERATURETBL WITH(NOLOCK)
									FROM DBO.TEMPERATURETBL WITH(NOLOCK) --2022.08 BK09로 백업정책 전환
									WHERE OFF_KEY = A.OFF_KEY
									AND TID_ID = A.TID_ID
									ORDER BY TPL_DATE DESC, TPL_TIME DESC
								) CUR

								ORDER BY ISNULL(P_GRP_ID, ''Z0000000000000'') DESC, P_GRP_NAME, GRP_NAME, OFF_NAME, CM_NUMBER
								--, ISSUE_CNT
								OFFSET (' + CONVERT(VARCHAR, @ROWS) + ' * ' + CONVERT(VARCHAR, @PAGE) + ') - ' + CONVERT(VARCHAR, @ROWS) + ' ROWS
								FETCH NEXT ' + CONVERT(VARCHAR, @ROWS) + ' ROW ONLY
								'
		END

	exec master.dbo.sp_print @SQL
	EXEC SP_EXECUTESQL @SQL
END
