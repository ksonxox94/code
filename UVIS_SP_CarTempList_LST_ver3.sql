	@GRP_ID			VARCHAR(14),		-- 그룹아이디2
	@CM_NUMBER		VARCHAR(30),		-- 차량번호
	@TID_ID         VARCHAR(11),		-- 단말기 번호
	@WORK_GUBUN		CHAR(6) = ''		-- 업무구분
