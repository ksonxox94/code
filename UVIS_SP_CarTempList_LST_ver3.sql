ALTER PROCEDURE [dbo].[UVIS_SP_CarTempList_LST_ver3] (
	@GUBUN			CHAR(1),			-- 구분
	@PAGE			INT,				-- 페이지번호
	@ROWS			INT,				-- 목록수

	@SS_OFF_KEY		VARCHAR(9),			-- 로그인 업체키
	@SS_CUST_ID		VARCHAR(14),		-- 로그인 기업ID
	@SS_I_USERID	VARCHAR(50),		-- 로그인 사용자ID
	@SS_I_CODE		CHAR(6),			-- 로그인 권한
	
	@CARGO_YN		CHAR(1) = 'N',		-- 창고여부 (Y=창고, N=차량)
	@START_DATE		VARCHAR(8),			-- 시작일자
	@END_DATE		VARCHAR(8),			-- 종료일자
	@START_TIME		VARCHAR(2),			-- 시간대 시작
	@END_TIME		VARCHAR(2),			-- 시간대 종료

