SET NOCOUNT ON

BEGIN
	SET @SQL = ''
	
	BEGIN
		SET @SQL = ' WITH BASEDATA AS (
											SELECT
												BA.*,
												-- A
												ALARM_A.DEVICE_TYPE AS DEVICE_TYPE_A,
												ALARM_A.ISSUE_TEMP_ADJUST AS ISSUE_TEMP_ADJUST_A,
												ALARM_A.ISSUE_PERSIST_TIME AS ISSUE_PERSIST_TIME_A,
												ALARM_A.ISSUE_STEP01_REPEAT AS ISSUE_STEP01_REPEAT_A,
												ALARM_A.ISSUE_STEP01_GROUP AS ISSUE_STEP01_GROUP_A,
												ALARM_A.ISSUE_STEP01_RESTORE AS ISSUE_STEP01_RESTORE_A,
												ALARM_A.ISSUE_STEP02_REPEAT AS ISSUE_STEP02_REPEAT_A,
												ALARM_A.ISSUE_STEP02_GROUP AS ISSUE_STEP02_GROUP_A,
												ALARM_A.ISSUE_STEP02_RESTORE AS ISSUE_STEP02_RESTORE_A,
												TIME_A.ALARM_TIME_A,
												-- B
												ALARM_B.DEVICE_TYPE AS DEVICE_TYPE_B,
												ALARM_B.ISSUE_TEMP_ADJUST AS ISSUE_TEMP_ADJUST_B,
												ALARM_B.ISSUE_PERSIST_TIME AS ISSUE_PERSIST_TIME_B,
												ALARM_B.ISSUE_STEP01_REPEAT AS ISSUE_STEP01_REPEAT_B,
												ALARM_B.ISSUE_STEP01_GROUP AS ISSUE_STEP01_GROUP_B,
												ALARM_B.ISSUE_STEP01_RESTORE AS ISSUE_STEP01_RESTORE_B,
												ALARM_B.ISSUE_STEP02_REPEAT AS ISSUE_STEP02_REPEAT_B,
												ALARM_B.ISSUE_STEP02_GROUP AS ISSUE_STEP02_GROUP_B,
												ALARM_B.ISSUE_STEP02_RESTORE AS ISSUE_STEP02_RESTORE_B,
												TIME_B.ALARM_TIME_B,
												-- C
												ALARM_C.DEVICE_TYPE AS DEVICE_TYPE_C,
												ALARM_C.ISSUE_TEMP_ADJUST AS ISSUE_TEMP_ADJUST_C,
												ALARM_C.ISSUE_PERSIST_TIME AS ISSUE_PERSIST_TIME_C,
												ALARM_C.ISSUE_STEP01_REPEAT AS ISSUE_STEP01_REPEAT_C,
												ALARM_C.ISSUE_STEP01_GROUP AS ISSUE_STEP01_GROUP_C,
												ALARM_C.ISSUE_STEP01_RESTORE AS ISSUE_STEP01_RESTORE_C,
												ALARM_C.ISSUE_STEP02_REPEAT AS ISSUE_STEP02_REPEAT_C,
												ALARM_C.ISSUE_STEP02_GROUP AS ISSUE_STEP02_GROUP_C,
												ALARM_C.ISSUE_STEP02_RESTORE AS ISSUE_STEP02_RESTORE_C,
												TIME_C.ALARM_TIME_C,
												-- D
												ALARM_D.DEVICE_TYPE AS DEVICE_TYPE_D,
												ALARM_D.ISSUE_TEMP_ADJUST AS ISSUE_TEMP_ADJUST_D,
												ALARM_D.ISSUE_PERSIST_TIME AS ISSUE_PERSIST_TIME_D,
												ALARM_D.ISSUE_STEP01_REPEAT AS ISSUE_STEP01_REPEAT_D,
												ALARM_D.ISSUE_STEP01_GROUP AS ISSUE_STEP01_GROUP_D,
												ALARM_D.ISSUE_STEP01_RESTORE AS ISSUE_STEP01_RESTORE_D,
												ALARM_D.ISSUE_STEP02_REPEAT AS ISSUE_STEP02_REPEAT_D,
												ALARM_D.ISSUE_STEP02_GROUP AS ISSUE_STEP02_GROUP_D,
												ALARM_D.ISSUE_STEP02_RESTORE AS ISSUE_STEP02_RESTORE_D,
												TIME_D.ALARM_TIME_D
											FROM
												(
													SELECT
														CONVERT(INT, ROW_NUMBER() OVER( ORDER BY ISNULL(P_GRP_ID, ''Z0000000000000'') DESC, P_GRP_NAME, GRP_NAME, OFF_NAME, CM_NUMBER)) RNUM,
														P_GRP_ID,
														P_GRP_NAME,
														GRP_ID,
														GRP_NAME,
														BA.OFF_NAME,
														BA.OFF_KEY,
														WORK_GUBUN AS WORK_GUBUN_CODE,
														N_CODENAME AS WORK_GUBUN_NAME,
														CM_KEY,
														CM_NUMBER,
														BA.TID_ID,
														TEMP_MIN_A, TEMP_MAX_A, TEMP_MIN_B, TEMP_MAX_B, TEMP_MIN_C, TEMP_MAX_C, TEMP_MIN_D, TEMP_MAX_D,
														MAX(ISNULL(BA.TOT_CNT, 0)) AS TOT_CNT,
														MAX(ISNULL(BA.ISSUE_CNT, 0)) AS ISSUE_CNT
													FROM (
														SELECT
															ISNULL(L1.P_GRP_ID, ''00000000000000'') AS P_GRP_ID,		-- 상위그룹
															ISNULL(L1.P_GRP_NAME, ''그룹미지정'') AS P_GRP_NAME, 		-- 상위그룹명
															ISNULL(L1.GRP_ID, ''00000000000000'') AS GRP_ID,		    -- 그룹
															ISNULL(L1.GRP_NAME, ''그룹미지정'') AS GRP_NAME,	        -- 그룹명
															TID.OFF_KEY,
															OT.OFF_NAME,
															TID.TID_ID,
															CT.CM_KEY,
															CT.CM_NUMBER,
															CT.WORK_GUBUN,
															CODE.N_CODENAME,
															-- A
															TEMP_A.TEMP_MIN AS TEMP_MIN_A,
															TEMP_A.TEMP_MAX AS TEMP_MAX_A,
															-- B
															TEMP_B.TEMP_MIN AS TEMP_MIN_B,
															TEMP_B.TEMP_MAX AS TEMP_MAX_B,
															-- C
															TEMP_C.TEMP_MIN AS TEMP_MIN_C,
															TEMP_C.TEMP_MAX AS TEMP_MAX_C,
															-- D
															TEMP_D.TEMP_MIN AS TEMP_MIN_D,
															TEMP_D.TEMP_MAX AS TEMP_MAX_D,
															ISNULL(TEM_AGG.TOT_CNT, 0) AS TOT_CNT,
															ISNULL(TEM_AGG.ISSUE_CNT, 0) AS ISSUE_CNT
														FROM Vehicle_System.dbo.TERMINALIDTBL TID WITH(NOLOCK)      -- 단말기 정보
														INNER JOIN Vehicle_System.dbo.CustomerOfficeRel COR WITH(nolock)	-- 관계 업체정보
															ON COR.OFF_KEY = TID.OFF_KEY
															AND COR.DEL_YN = ''N''
														LEFT OUTER JOIN Vehicle_System.dbo.CARMASTERTBL CT WITH(NOLOCK)  -- 차량정보
															ON CT.OFF_KEY = TID.OFF_KEY
															AND CT.TID_ID = TID.TID_ID
															AND CT.CM_DEL_FLAG = ''1''
